const FILE_BASENAME = "syncresume-optimized-resume";
export async function downloadDocx(resume: StructuredResume) {
  const { Document, Packer } = await import("docx");
  const document = new Document({
    creator: "SyncResume",
    title: "Optimized resume",
    description: "ATS-safe resume generated by SyncResume.",
    sections: [
      {
        properties: {},
        children: await buildDocxParagraphs(resume),
      },
    ],
  });
  const blob = await Packer.toBlob(document);
  saveBlob(blob, `${FILE_BASENAME}.docx`);
}

export async function downloadPdf(resume: StructuredResume) {
  const { jsPDF } = await import("jspdf");
  const pdf = new jsPDF({ unit: "pt", format: "letter" });
  const page = {
    width: pdf.internal.pageSize.getWidth(),
    height: pdf.internal.pageSize.getHeight(),
    margin: 54,
  };
  const maxWidth = page.width - page.margin * 2;
  let y = page.margin;

  const addPageIfNeeded = (height = 16) => {
    if (y + height > page.height - page.margin) {
      pdf.addPage();
      y = page.margin;
    }
  };

  const addHeading = (text: string) => {
    addPageIfNeeded(22);
    pdf.setFont("helvetica", "bold");
    pdf.setFontSize(11);
    pdf.text(text.toUpperCase(), page.margin, y);
    y += 18;
  };

  const addText = (text: string, options: { bold?: boolean; indent?: number } = {}) => {
    pdf.setFont("helvetica", options.bold ? "bold" : "normal");
    pdf.setFontSize(10);
    const indent = options.indent ?? 0;
    const lines = pdf.splitTextToSize(text, maxWidth - indent);
    for (const line of lines) {
      addPageIfNeeded(14);
      pdf.text(line, page.margin + indent, y);
      y += 14;
    }
  };

  if (resume.summary.trim()) {
    addHeading("Summary");
    addText(resume.summary.trim());
    y += 10;
  }

  if (resume.experience.length > 0) {
    addHeading("Experience");
    for (const role of resume.experience) {
      const heading = [role.title, role.company, role.location, role.dates]
        .map((part) => part.trim())
        .filter(Boolean)
        .join(" | ");
      if (heading) {
        addText(heading, { bold: true });
      }
      for (const bullet of role.bullets) {
        addText(`- ${bullet}`, { indent: 12 });
      }
      y += 8;
    }
  }

  if (resume.skills.length > 0) {
    addHeading("Skills");
    addText(resume.skills.join(", "));
    y += 10;
  }

  if (resume.education.length > 0) {
    addHeading("Education");
    for (const item of resume.education) {
      addText(item);
    }
  }

  pdf.save(`${FILE_BASENAME}.pdf`);
}

export async function downloadTextPdf(text: string, fileName = "syncresume-extracted-resume.pdf") {
  const { jsPDF } = await import("jspdf");
  const pdf = new jsPDF({ unit: "pt", format: "letter" });
  const page = {
    width: pdf.internal.pageSize.getWidth(),
    height: pdf.internal.pageSize.getHeight(),
    margin: 54,
  };
  const maxWidth = page.width - page.margin * 2;
  let y = page.margin;

  pdf.setFont("helvetica", "normal");
  pdf.setFontSize(10);

  const addPageIfNeeded = (height = 14) => {
    if (y + height > page.height - page.margin) {
      pdf.addPage();
      y = page.margin;
    }
  };

  const normalizedText = text.replace(/\r/g, "").replace(/\n{3,}/g, "\n\n").trim();
  for (const paragraph of normalizedText.split("\n")) {
    const line = paragraph.trim();
    if (!line) {
      y += 8;
      continue;
    }

    const lines = pdf.splitTextToSize(line, maxWidth);
    for (const wrappedLine of lines) {
      addPageIfNeeded();
      pdf.text(wrappedLine, page.margin, y);
      y += 14;
    }
  }

  pdf.save(sanitizeDownloadFileName(fileName));
}

export async function downloadResumeDocumentPdf(
  resumeDocument: ResumeDocument,
  templateId: ResumeTemplateId,
  fileName = "syncresume-resume.pdf",
  fontId?: ResumeFontId,
) {
  const { jsPDF } = await import("jspdf");
  const template = getResumeTemplateDefinition(templateId);
  const font = fontId ? getResumeFontOption(fontId) : null;
  const pdfFont = getPdfFontFamily(font?.id);
  const blocks = getTemplateDocxBlocks(resumeDocument, templateId);
  const pdf = new jsPDF({ unit: "pt", format: "letter", orientation: "portrait" });
  const page = {
    width: pdf.internal.pageSize.getWidth(),
    height: pdf.internal.pageSize.getHeight(),
    margin: Math.max(42, Math.min(64, template.pdf.margin)),
  };
  const maxWidth = page.width - page.margin * 2;
  const bodySize = template.pdf.fontSize;
  const headingSize = template.pdf.headingSize;
  const lineHeight = Math.max(12, template.pdf.lineHeight);
  const sectionGap = Math.max(10, template.pdf.sectionGap);
  let y = page.margin;

  const addPageIfNeeded = (height = lineHeight) => {
    if (y + height <= page.height - page.margin) return;
    pdf.addPage();
    y = page.margin;
  };

  const addWrappedText = (
    text: string,
    options: { bold?: boolean; center?: boolean; indent?: number; size?: number } = {},
  ) => {
    const size = options.size ?? bodySize;
    const indent = options.indent ?? 0;
    pdf.setFont(pdfFont, options.bold ? "bold" : "normal");
    pdf.setFontSize(size);

    const lines = pdf.splitTextToSize(text, maxWidth - indent);
    for (const line of lines) {
      addPageIfNeeded(lineHeight);
      const x = options.center
        ? page.width / 2
        : page.margin + indent;
      if (options.center) {
        pdf.text(line, x, y, { align: "center" });
      } else {
        pdf.text(line, x, y);
      }
      y += lineHeight;
    }
  };

  const addHeading = (text: string) => {
    y += 4;
    addPageIfNeeded(headingSize + 8);
    pdf.setTextColor(...template.pdf.accent);
    addWrappedText(text.toUpperCase(), { bold: true, size: headingSize });
    pdf.setTextColor(0, 0, 0);
    y += 2;
  };

  for (const block of blocks) {
    if (block.isContact) {
      const { name, details } = parseResumeContact(block.lines.join(" | "), resumeDocument.title);
      if (name) addWrappedText(name, { bold: true, center: true, size: bodySize + 5 });
      if (details.length > 0) {
        addWrappedText(details.map(formatContactDetailDisplay).join(" | "), { center: true, size: bodySize - 1 });
      }
      y += sectionGap;
      continue;
    }

    addHeading(block.title);
    for (const line of block.lines) {
      const isBullet = /^\s*[-•]\s+/.test(line);
      addWrappedText(isBullet ? line.replace(/^\s*[-•]\s+/, "- ") : line, {
        indent: isBullet ? 12 : 0,
        bold: !isBullet && block.type === "experience" && /\b(?:19|20)\d{2}\b/.test(line),
      });
    }
    y += sectionGap;
  }

  pdf.save(sanitizeDownloadFileName(fileName));
}

function getPdfFontFamily(fontId?: ResumeFontId): "helvetica" | "times" | "courier" {
  if (fontId === "serif") return "times";
  if (fontId === "mono") return "courier";
  return "helvetica";
}

export async function downloadResumeDocumentDocx(
  document: ResumeDocument,
  templateId: ResumeTemplateId,
  fileName = "syncresume-resume.docx",
  fontId?: ResumeFontId,
) {
  const { Document, Packer, Paragraph, TextRun } = await import("docx");
  const template = getResumeTemplateDefinition(templateId);
  const font = fontId ? getResumeFontOption(fontId) : null;
  const docxFont = font?.docxFamily;
  const blocks = getTemplateDocxBlocks(document, templateId);
  const paragraphs: InstanceType<typeof Paragraph>[] = [];
  const addText = (text: string, options: { bold?: boolean; center?: boolean; size?: number } = {}) => {
    paragraphs.push(
      new Paragraph({
        alignment: options.center ? "center" : undefined,
        spacing: { after: template.density === "compact" ? 50 : 80 },
        children: [new TextRun({ text, bold: options.bold, size: options.size ?? 21, font: docxFont })],
      }),
    );
  };
  const addHeading = (text: string) => {
    paragraphs.push(
      new Paragraph({
        spacing: { before: template.density === "compact" ? 120 : 180, after: 80 },
        children: [new TextRun({ text: text.toUpperCase(), bold: true, size: 22, font: docxFont })],
      }),
    );
  };

  for (const block of blocks) {
    if (block.isContact) {
      const { name, details } = parseResumeContact(block.lines.join(" | "), document.title);
      if (name) addText(name, { bold: true, center: true, size: 24 });
      if (details.length > 0) {
        addText(details.map(formatContactDetailDisplay).join(" | "), { center: true, size: 19 });
      }
      continue;
    }

    addHeading(block.title);
    for (const line of block.lines) {
      addText(line.replace(/^[-•]\s*/, ""));
    }
  }

  const docx = new Document({
    creator: "SyncResume",
    title: document.title,
    description: `${template.name} resume generated by SyncResume.`,
    sections: [{ properties: {}, children: paragraphs }],
  });
  const blob = await Packer.toBlob(docx);
  saveBlob(blob, sanitizeDownloadFileName(fileName).replace(/\.pdf$/, ".docx"));
}

export async function copyPlainText(resume: StructuredResume) {
  await navigator.clipboard.writeText(resumeToPlainText(resume));
}

async function buildDocxParagraphs(resume: StructuredResume) {
  const { Paragraph, TextRun } = await import("docx");
  const paragraphs: InstanceType<typeof Paragraph>[] = [];
  const addHeading = (text: string) => {
    paragraphs.push(
      new Paragraph({
        spacing: { before: 180, after: 80 },
        children: [new TextRun({ text: text.toUpperCase(), bold: true, size: 22 })],
      }),
    );
  };
  const addText = (text: string, options: { bold?: boolean } = {}) => {
    paragraphs.push(
      new Paragraph({
        spacing: { after: 80 },
        children: [new TextRun({ text, bold: options.bold, size: 21 })],
      }),
    );
  };

  if (resume.summary.trim()) {
    addHeading("Summary");
    addText(resume.summary.trim());
  }

  if (resume.experience.length > 0) {
    addHeading("Experience");
    for (const role of resume.experience) {
      const heading = [role.title, role.company, role.location, role.dates]
        .map((part) => part.trim())
        .filter(Boolean)
        .join(" | ");
      if (heading) {
        addText(heading, { bold: true });
      }
      for (const bullet of role.bullets) {
        addText(`- ${bullet}`);
      }
    }
  }

  if (resume.skills.length > 0) {
    addHeading("Skills");
    addText(resume.skills.join(", "));
  }

  if (resume.education.length > 0) {
    addHeading("Education");
    for (const item of resume.education) {
      addText(item);
    }
  }

  return paragraphs;
}

function saveBlob(blob: Blob, fileName: string) {
  const url = URL.createObjectURL(blob);
  const anchor = document.createElement("a");
  anchor.href = url;
  anchor.download = fileName;
  anchor.click();
  URL.revokeObjectURL(url);
}

function sanitizeDownloadFileName(value: string): string {
  const normalized = value.trim().replace(/\.[^.]+$/, "").replace(/[^a-zA-Z0-9._-]+/g, "-");
  return `${normalized || "syncresume-extracted-resume"}.pdf`;
}
